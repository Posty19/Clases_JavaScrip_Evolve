/* 7️⃣ Manipulación de un Contador con ++ y --

Dado un objeto contador con la propiedad valor, crea una función que:

Incremente valor en 2 si es par.

Decremente valor en 1 si es impar.

Devuelva el objeto actualizado. */

const obj = {contador:5}

const contador = objeto => obj.contador%2===0 ? obj.contador++:obj.contador--;

console.log(obj);
contador(obj);
console.log(obj);
contador(obj);
console.log(obj);
contador(obj);
console.log(obj);
contador(obj);
console.log(obj);